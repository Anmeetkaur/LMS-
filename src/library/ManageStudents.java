    private void rSMaterialButtonCircle1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle1ActionPerformed
        // TODO add your handling code here:
        if(txt_studentid.getText().isEmpty()||txt_name.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{ 
            try{
             Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3305/library","root","abcd");
            
            PreparedStatement pst = con.prepareStatement("Insert into student values(?,?,?,?)");
            
            pst.setInt(1, Integer.valueOf(txt_studentid.getText()));
            pst.setString(2, txt_name.getText());
            pst.setString(3, ComboBox_course.getSelectedItem().toString());
            pst.setString(4, ComboBox_branch.getSelectedItem().toString());          
            int updatedRowCount = pst.executeUpdate();
            
            if(updatedRowCount > 0){
                JOptionPane.showMessageDialog(this, "Record Inserted Successfully");
                setStudentDetailsToTable();
                ManageStudents book = new ManageStudents();
                book.setVisible(true);
                dispose();
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Failure inserting record");
            }
         }catch(Exception e){
                 e.printStackTrace();
          }
               
        }
    }//GEN-LAST:event_rSMaterialButtonCircle1ActionPerformed

