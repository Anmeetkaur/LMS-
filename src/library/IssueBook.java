    public void getBookDetails(){
        int bookId = Integer.parseInt(txt_BOOKID.getText());
       try{
          Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3305/library","root","abcd");
            String sql = "select * from bookdetail where BookId ="+bookId;
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                txt_bookid.setText(rs.getString("BookId"));
                txt_bookname.setText(rs.getString("Name"));
                txt_author.setText(rs.getString("Author"));
                txt_quantity.setText(rs.getString("Quantity"));
            }
            else{
               
                     JOptionPane.showMessageDialog(this,"Invalid Book Id");  
            }
       }catch(Exception e){
           e.printStackTrace();
       }
    }
    
    
    
    
    
