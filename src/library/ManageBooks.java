    private void rSMaterialButtonCircle1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle1ActionPerformed
        // TODO add your handling code here:
        if(txt_bookid.getText().isEmpty()||txt_bookname.getText().isEmpty()||txt_authorname.getText().isEmpty()||txt_bookquantity.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{ 
            try{
             Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3305/library","root","abcd");
            
            PreparedStatement pst = con.prepareStatement("Insert into bookdetail values(?,?,?,?)");
            
            pst.setInt(1, Integer.valueOf(txt_bookid.getText()));
            pst.setString(2, txt_bookname.getText());
            pst.setString(3, txt_authorname.getText());
            pst.setInt(4, Integer.valueOf(txt_bookquantity.getText()));
            
            int updatedRowCount = pst.executeUpdate();
            
            if(updatedRowCount > 0){
                JOptionPane.showMessageDialog(this, "Record Inserted Successfully");
       
                setBookDetailsToTable();
                ManageBooks book = new ManageBooks();
                book.setVisible(true);
                dispose();
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Failure inserting record");
            }
         }catch(Exception e){
                 e.printStackTrace();
          }
               
        }
    }//GEN-LAST:event_rSMaterialButtonCircle1ActionPerformed

