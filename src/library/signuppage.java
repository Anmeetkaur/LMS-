    private void rSMaterialButtonCircle1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle1ActionPerformed
        // TODO add your handling code here:
        if(txt_username.getText().isEmpty()||txt_password.getText().isEmpty()||txt_email.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else{
        try{
             Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3305/library","root","abcd");
            
            PreparedStatement pst = con.prepareStatement("Insert into login values(?,?,?,?)");
            
            pst.setString(1, txt_username.getText());
            pst.setString(2, txt_password.getText());
            pst.setString(3, txt_email.getText());
            pst.setInt(4, Integer.valueOf(txt_contact.getText()));
            
            int updatedRowCount = pst.executeUpdate();
            
            if(updatedRowCount > 0){
                JOptionPane.showMessageDialog(this, "Record Inserted Successfully");
                Loginpage log = new Loginpage();
                log.setVisible(true);
                dispose();
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Failure inserting record");
            }
         }catch(Exception e){
                 e.printStackTrace();
          }
        }
    }//GEN-LAST:event_rSMaterialButtonCircle1ActionPerformed

