    public void login(){
        String name = txt_username.getText();
        String pwd = txt_password.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3305/library","root","abcd");
            
            PreparedStatement pst = con.prepareStatement("select * from login where name = ? and password = ?");
            pst.setString(1,name);
            pst.setString(2, pwd);
            
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "Login Successfully");
                Homepage home = new Homepage();
                home.setVisible(true);
                this.dispose();
            }
            else{
                 JOptionPane.showMessageDialog(this, "Incorrect Username/Password");
            }
        }catch(Exception e){
           e.printStackTrace();
        }
    }

