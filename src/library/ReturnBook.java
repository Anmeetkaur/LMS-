    public int getIssueDetails(){
        int bookId = Integer.parseInt(txt_BOOKID.getText());
       try{
          Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3305/library","root","abcd");
            String sql = "select * from issue_detail where BookId ="+bookId;
            PreparedStatement pst = con.prepareStatement(sql);
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                txt_studentname.setText(rs.getString("StudentName"));
                txt_bookname.setText(rs.getString("BookName"));
                txt_issuedate.setText(rs.getString("IssueDate"));
                txt_duedate.setText(rs.getString("DueDate"));
                return 1;
            }
            else{
               
                     JOptionPane.showMessageDialog(this,"Invalid Book Id");  
            }
       }catch(Exception e){
           e.printStackTrace();
       }
       return 0;
    }
    
    
    
    
 

